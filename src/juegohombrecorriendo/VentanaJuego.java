  protected void iniciarODetener(String estado) {
      
    boolean estaDetenido = estado.equals("Iniciar");
    btnIniciar.setText(estaDetenido ? "Pausar" : "Iniciar");
    System.out.print("\n--- working = " + estaDetenido + " ---\n");
    if (estaDetenido) {
      juego.iniciar();
    } else {
      juego.pausar();
    }
  }

